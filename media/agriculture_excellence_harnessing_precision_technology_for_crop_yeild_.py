def validate_inputs(pH, temperature, humidity, rainfall):
    if pH < 3.5:
        return False, f"Invalid pH: {pH} — Too acidic. Crops cannot grow well in highly acidic soil."
    elif pH > 9.5:
        return False, f"Invalid pH: {pH} — Too alkaline. Most crops fail to absorb nutrients properly."

    if humidity < 0 or humidity > 100:
        return False, f"Invalid Humidity: {humidity}% — Humidity must be between 0% and 100%."

    if temperature < 0 or temperature > 60:
        return False, f"Invalid Temperature: {temperature}°C — Extreme temperatures are not suitable for crop growth."

    if rainfall < 0:
        return False, f"Invalid Rainfall: {rainfall}mm — Rainfall can't be negative."
    elif rainfall > 350:
        return False, f"Invalid Rainfall: {rainfall}mm — Too much rainfall may indicate flood conditions."

    return True, "Inputs are valid."


